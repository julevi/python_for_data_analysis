# OPERATORS IN PYTHON

# Arithmetic operators
x = 10
y = 3
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Integer Division:", x // y)
print("Modulus:", x % y)
print("Exponent:", x ** y)

print("-----------")

# Comparison operators
print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)

print("-----------")

# Logical operators
print("x > 5 and y < 5:", x > 5 and y < 5)
print("x > 5 or y > 5:", x > 5 or y > 5)
print("not(x > 5):", not(x > 5))
